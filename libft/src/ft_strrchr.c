char	*ft_strrchr(const char *s, int c)
{
	int	con;

	con = 0;
	while (s[con])
		con++;
	while (con >= 0)
	{
		if (s[con] == (char )c)
			return ((char *)(s + con));
		con--;
	}
	return (0);
}
